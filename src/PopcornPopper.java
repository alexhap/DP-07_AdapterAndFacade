public class PopcornPopper {
    public void on() {
        System.out.println("Popcorn Popper turned on");
    }

    public void pop() {
        System.out.println("Popper starts to cook popcorn");
    }

    public void off() {
        System.out.println("Popcorn Popper turned off");
    }
}
